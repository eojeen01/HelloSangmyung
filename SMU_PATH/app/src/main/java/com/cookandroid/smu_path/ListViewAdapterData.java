package com.cookandroid.smu_path;

public class ListViewAdapterData {

    public String Academic_num; //학수번호
    public String Lecture; //교과목명
    public String School_year; //학년
    public String Professor; //담당교수
    public String Department; //개설학부
    public String Time; //강의시간
    public String Lecture_room; //강의실

    public void setAcademic_num(String Academic_num){this.Academic_num = Academic_num;}
    public void setLecture(String Lecture){this.Lecture = Lecture;}
    public void setSchool_year(String School_year){this.School_year = School_year;}
    public void setProfessor(String Professor){this.Professor = Professor;}
    public void setDepartment(String Department){this.Department = Department;}
    public void setTime(String Time){this.Time = Time;}
    public void setLecture_room(String Lecture_room){this.Lecture_room = Lecture_room;}

    public String getAcademic_num(){return this.Academic_num;}
    public String getLecture(){return this.Lecture;}
    public String getSchool_year(){return this.School_year;}
    public String getProfessor(){return this.Professor;}
    public String getDepartment(){return this.Department;}
    public String getTime(){return this.Time;}
    public String getLecture_room(){return this.Lecture_room;}

}
